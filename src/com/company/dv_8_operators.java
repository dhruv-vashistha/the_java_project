public class dv_8_operators {
    public static void main(String[] args) {

        int a = 8;
        int b = a - 4;
        int c = a % 3;            //module operator tells the reminder.
        int k = 12;
        k += 5;
        int j = 7;
        j *= 2;
        System.out.println(k);
        System.out.println(j);
        System.out.println(6==7);    //comparison type operators
        System.out.println(7>=8);
        System.out.println(1>2 && 5<4);   //logical operators
        System.out.println(1<2 || 7>88);  // operator called or.


    }
}
